package com.java.may;

public class Car {
	
	int carNo;
	String carName;
	String carColor;
	
	public Car(int carNo, String carName, String carColor) {
		super();
		this.carNo = carNo;
		this.carName = carName;
		this.carColor = carColor;
	}

	public int getCarNo() {
		return carNo;
	}

	public String getCarName() {
		return carName;
	}

	public String getCarColor() {
		return carColor;
	}
	
	
}
